/** Zend_View_Helper */
require_once 'Zend/View/Helper/Abstract.php';

/**
 * Helper for showing message generated by ZLayer_Controller_Action_Messenger
 *
 * @package    Zend_View
 * @subpackage Helper
 * @author  João Paulo Faria (jpfaria at gmail dot com)
 * @license    http://framework.zend.com/license/new-bsd     New BSD License
 */
class ZLayer_View_Helper_Request extends Zend_View_Helper_Abstract
{
    /**
     * Tables primary and foreign keys
     * 
     * @var Zend_Controller_Front
     */
    protected $_front;
    
    
	/**
	 * Callback
	 * 
	 * @return ZLayer_View_Helper_Request
	 */
	public function request(){
	    $this->_front = Zend_Controller_Front::getInstance();
		return $this;
	}
	
	/**
	 * Return module name
	 * 
	 * @return string
	 */
    public function getModuleName()
    {
        return $this->_front->getRequest()->getModuleName();
    }
    
	/**
	 * Return controller name
	 * 
	 * @return string
	 */
    public function getControllerName()
    {
        return $this->_front->getRequest()->getControllerName();
    }
    
    /**
	 * Return action name
	 * 
	 * @return string
	 */
    public function getActionName()
    {
        return $this->_front->getRequest()->getActionName();
    }
    
    /**
     * Return params
     *
     * @return array
     */
    public function getParams()
    {
        return $this->_front->getRequest()->getParams();
    }
    
    /**
     * Return the param
     *
     * @param string $param
     * @return string
     */
    public function getParam($param)
    {
        return $this->_front->getRequest()->getParam($param);
    }
    
}    
    